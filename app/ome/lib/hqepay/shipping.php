    /**
     * 获取LogiNameType
     * @param mixed $shipping shipping
     * @param mixed $shop_type shop_type
     * @return mixed 返回结果
     */
    public function getLogiNameType($shipping, $shop_type) {
        if($shop_type == 'pinduoduo') {
            return $this->pinduoduo[$shipping] ? : [];
        }
        if($shop_type == 'luban') {
            return $this->luban[$shipping] ? : [];
        }
        if($this->default[$shipping]) {
            return ['logi_name'=>$shipping, 'logi_type'=>$this->default[$shipping]];
        }
        return [];
    }
    
    /**
     * 获取Default
     * @param mixed $code code
     * @return mixed 返回结果
     */
    public function getDefault($code)
    {
        return array_search($code, $this->default);
    }
